function u0_anchor_terminal_export_test()
% Regression test for standardized terminal-state exports in multi-level runs.

    opts = struct( ...
        'N_values', [2, 4], ...
        'p_values', [3, 5], ...
        'run_tag', 'terminal_export_test', ...
        'show_figures', false, ...
        'acceptance_policy', 'diagnostic_only');

    out = u0_anchor_monge_ampere_multiple_level_demo('paper_example_5_1', opts);
    T = out.level_summary;

    assert(isequal(T.N.', [2, 4]), 'Expected stage degrees N = [2, 4].');
    assert(all(T.Success), 'The minimal multiple-level paper_example_5_1 run should succeed.');

    base_dir = fullfile(pwd, 'output', ...
        'u0_anchor_monge_ampere_multiple_level_paper_example_5_1_terminal_export_test', ...
        'per_level');

    for k = 1:numel(T.N)
        run_dir = fullfile(base_dir, sprintf('level_%02d_N_%03d', k, T.N(k)));
        assert(isfolder(run_dir), sprintf('Missing run directory: %s', run_dir));

        mat_path = fullfile(run_dir, 'terminal_state.mat');
        csv_path = fullfile(run_dir, 'terminal_metrics.csv');
        txt_path = fullfile(run_dir, 'u0_definition.txt');

        assert(isfile(mat_path), sprintf('Missing terminal MAT export: %s', mat_path));
        assert(isfile(csv_path), sprintf('Missing terminal CSV export: %s', csv_path));
        assert(isfile(txt_path), sprintf('Missing terminal definition export: %s', txt_path));

        mat_data = load(mat_path);
        required_fields = { ...
            'U_final', ...
            'u_num_eval', ...
            'u0_eval', ...
            'x_eval', ...
            'y_eval', ...
            't_final', ...
            'residual_final', ...
            'jacobian_cond_final', ...
            'psd_ratio_final', ...
            'indef_ratio_final', ...
            'anchor_label', ...
            'acceptance_policy', ...
            'convexity_mode', ...
            'line_search_allow_fallback'};
        for i = 1:numel(required_fields)
            assert(isfield(mat_data, required_fields{i}), ...
                sprintf('terminal_state.mat is missing field %s.', required_fields{i}));
        end

        metrics = readtable(csv_path, 'TextType', 'string');
        assert(height(metrics) == 1, 'terminal_metrics.csv should contain exactly one row.');
        required_vars = { ...
            't_final', ...
            'residual_final', ...
            'jacobian_cond_final', ...
            'psd_ratio_final', ...
            'indef_ratio_final', ...
            'acceptance_policy', ...
            'convexity_mode', ...
            'line_search_allow_fallback'};
        for i = 1:numel(required_vars)
            assert(ismember(required_vars{i}, metrics.Properties.VariableNames), ...
                sprintf('terminal_metrics.csv is missing column %s.', required_vars{i}));
        end
        assert(strcmp(metrics.acceptance_policy(1), "diagnostic_only"), ...
            'terminal_metrics.csv should record the requested acceptance policy.');
        assert(strcmp(metrics.convexity_mode(1), "none"), ...
            'terminal_metrics.csv should record convexity_mode = none for diagnostic-only runs.');
        assert(metrics.line_search_allow_fallback(1) == false, ...
            'terminal_metrics.csv should record that fallback is disabled.');

        definition_text = fileread(txt_path);
        assert(contains(definition_text, string(mat_data.anchor_label)), ...
            'u0_definition.txt should record the anchor label in human-readable form.');
        assert(contains(definition_text, "Acceptance policy: diagnostic_only"), ...
            'u0_definition.txt should record the acceptance policy.');
        assert(contains(definition_text, "Convexity mode: none"), ...
            'u0_definition.txt should record convexity mode.');
    end
end
