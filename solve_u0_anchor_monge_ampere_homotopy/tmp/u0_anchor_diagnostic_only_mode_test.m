function u0_anchor_diagnostic_only_mode_test()
% Regression test for explicit diagnostic-only acceptance mode metadata.

    opts = struct( ...
        'N_values', [2, 4], ...
        'p_values', [3, 5], ...
        'run_tag', 'diagnostic_only_mode_test', ...
        'show_figures', false, ...
        'acceptance_policy', 'diagnostic_only');

    out = u0_anchor_monge_ampere_multiple_level_demo('paper_example_5_1', opts);
    assert(all(cellfun(@(r) r.success, out.level_results)), ...
        'Expected the diagnostic-only mode smoke test to converge.');

    run_dir = fullfile(pwd, 'output', ...
        'u0_anchor_monge_ampere_multiple_level_paper_example_5_1_diagnostic_only_mode_test', ...
        'per_level', 'level_01_N_002');
    mat_data = load(fullfile(run_dir, 'terminal_state.mat'));

    assert(isfield(mat_data, 'acceptance_policy'), ...
        'Expected terminal_state.mat to record the acceptance policy.');
    assert(strcmp(mat_data.acceptance_policy, 'diagnostic_only'), ...
        'Expected the exported acceptance policy to match the requested research mode.');
    assert(strcmp(mat_data.convexity_mode, 'none'), ...
        'Expected diagnostic-only policy to export convexity_mode = none.');
    assert(~mat_data.line_search_allow_fallback, ...
        'Expected diagnostic-only policy to disable convexity-envelope fallback.');

    metrics = readtable(fullfile(run_dir, 'terminal_metrics.csv'), 'TextType', 'string');
    assert(strcmp(metrics.acceptance_policy(1), "diagnostic_only"), ...
        'Expected terminal_metrics.csv to record acceptance_policy.');
    assert(strcmp(metrics.convexity_mode(1), "none"), ...
        'Expected terminal_metrics.csv to record convexity_mode = none.');
    assert(metrics.line_search_allow_fallback(1) == false, ...
        'Expected terminal_metrics.csv to record fallback as disabled.');
end
