function u0_anchor_signed_rhs_external_anchor_test()
% Regression test for signed-RHS runs with an external u0 anchor.

    amplitude = 0.05;
    branch_u = @(x, y) amplitude * x .* (1 - x) .* y .* (1 - y);

    opts = struct( ...
        'N_values', [2, 4], ...
        'p_values', [3, 5], ...
        'run_tag', 'signed_rhs_external_anchor_test', ...
        'show_figures', false, ...
        'acceptance_policy', 'diagnostic_only', ...
        'u0_fun', branch_u, ...
        'anchor_label', 'signed polynomial branch');

    out = u0_anchor_monge_ampere_multiple_level_demo('signed_branch_polynomial', opts);
    assert(all(cellfun(@(r) r.success, out.level_results)), ...
        'Expected the signed-RHS external-anchor run to converge on every level.');
    assert(all(cellfun(@(r) r.reached_t_end, out.level_results)), ...
        'Expected the signed-RHS external-anchor run to reach the final continuation parameter.');

    level1 = out.level_results{1};
    expected_u0 = branch_u(level1.x_eval, level1.y_eval);
    level1_dir = fullfile(out.output_dir, 'per_level', 'level_01_N_002');
    terminal_state = load(fullfile(level1_dir, 'terminal_state.mat'));
    assert(level1.anchor_rhs_min_f < 0, ...
        'Expected the manufactured benchmark to have a sign-changing RHS.');
    assert(strcmp(terminal_state.anchor_label, 'signed polynomial branch'), ...
        'Expected the supplied external anchor label to propagate through the run.');
    assert(max(abs(level1.u0_eval(:) - expected_u0(:))) < 2e-2, ...
        'Expected the projected external branch anchor to remain close to the supplied function.');
end
