function u0_anchor_case_summary_demo()
% Batch summary for the anchored u0 homotopy demo cases.
%
% Runs the configured benchmark cases, collects the final diagnostics from
% each case history file, and writes a consolidated CSV/Markdown summary.

    cases = case_catalog();
    output_dir = fullfile(pwd, 'output', 'u0_anchor_case_summary');
    if ~exist(output_dir, 'dir')
        mkdir(output_dir);
    end

    num_cases = numel(cases);
    summary_rows = repmat(empty_summary_row(), num_cases, 1);

    for k = 1:num_cases
        spec = cases(k);
        fprintf('\n===== SUMMARY CASE %s =====\n', spec.case_name);
        u0_anchor_monge_ampere_homotopy_demo(spec.case_name);
        summary_rows(k) = collect_case_summary(spec);
    end

    summary_table = struct2table(summary_rows);
    writetable(summary_table, fullfile(output_dir, 'u0_anchor_case_summary.csv'));
    write_markdown_summary(summary_table, fullfile(output_dir, 'u0_anchor_case_summary.md'));

    disp(summary_table);
    fprintf('Summary written under %s\n', output_dir);
end

