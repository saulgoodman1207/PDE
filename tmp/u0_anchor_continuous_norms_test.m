function u0_anchor_continuous_norms_test()
% Regression test for paired (N,p) sweeps with continuous-norm summaries.

    opts = struct( ...
        'N_values', [2, 4], ...
        'p_values', [3, 5], ...
        'run_tag', 'cont_norm_test', ...
        'show_figures', false);

    out = u0_anchor_monge_ampere_homotopy_demo('paper_example_5_1', opts);
    T = out.summary_table;

    assert(isequal(T.N.', [2, 4]), 'Expected paired sweep to include N = [2, 4].');
    assert(isequal(T.TargetP.', [3, 5]), 'Expected paired sweep to include p = [3, 5].');
    assert(ismember('FinalContL2', T.Properties.VariableNames), ...
        'Summary table must include FinalContL2.');
    assert(ismember('FinalContH1', T.Properties.VariableNames), ...
        'Summary table must include FinalContH1.');
    assert(all(isfinite(T.FinalContL2)), 'Continuous L2 norms should be finite.');
    assert(all(isfinite(T.FinalContH1)), 'Continuous H1 norms should be finite.');
    assert(T.FinalContL2(2) < T.FinalContL2(1), ...
        'Continuous L2 norm should decrease between N = 2 and N = 4.');
    assert(T.FinalContH1(2) < T.FinalContH1(1), ...
        'Continuous H1 norm should decrease between N = 2 and N = 4.');
end
